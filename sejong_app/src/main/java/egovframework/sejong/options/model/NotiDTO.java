package egovframework.sejong.options.model;

public class NotiDTO {
	private int notiSeq;
	private String title;
	private String expln;
	private String useYn;
	private String postStr;
	private String postEnd;
	private String regId;
	private String regDtm;
	private String modId;
	private String modDtm;
	public int getNotiSeq() {
		return notiSeq;
	}
	public void setNotiSeq(int notiSeq) {
		this.notiSeq = notiSeq;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getExpln() {
		return expln;
	}
	public void setExpln(String expln) {
		this.expln = expln;
	}
	public String getUseYn() {
		return useYn;
	}
	public void setUseYn(String useYn) {
		this.useYn = useYn;
	}
	public String getPostStr() {
		return postStr;
	}
	public void setPostStr(String postStr) {
		this.postStr = postStr;
	}
	public String getPostEnd() {
		return postEnd;
	}
	public void setPostEnd(String postEnd) {
		this.postEnd = postEnd;
	}
	public String getRegId() {
		return regId;
	}
	public void setRegId(String regId) {
		this.regId = regId;
	}
	public String getRegDtm() {
		return regDtm;
	}
	public void setRegDtm(String regDtm) {
		this.regDtm = regDtm;
	}
	public String getModId() {
		return modId;
	}
	public void setModId(String modId) {
		this.modId = modId;
	}
	public String getModDtm() {
		return modDtm;
	}
	public void setModDtm(String modDtm) {
		this.modDtm = modDtm;
	}
	
}
